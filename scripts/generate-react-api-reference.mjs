import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import ts from "typescript";

const root = process.cwd();
const componentsDir = path.join(root, "packages/react/src/components");
const outputPath = path.join(root, "docs/reference/react-components.md");
const shouldWrite = process.argv.includes("--write");
const shouldCheck = process.argv.includes("--check");

if (!shouldWrite && !shouldCheck) {
  console.error(
    "Usage: node scripts/generate-react-api-reference.mjs --write|--check"
  );
  process.exit(1);
}

const componentFiles = (await readdir(componentsDir))
  .filter((fileName) => fileName.endsWith(".tsx"))
  .filter((fileName) => !fileName.endsWith(".test.tsx"))
  .sort();
const sections = [];

for (const fileName of componentFiles) {
  sections.push(await generateComponentSection(fileName));
}

const generated = `# React Component API Reference

<!-- Generated by scripts/generate-react-api-reference.mjs. Do not edit by hand. -->

This reference is generated from TypeScript source in
\`packages/react/src/components\`. It complements the component contracts under
\`docs/agent/components\`: contracts explain behavior and guidance, while this
file lists current exports, prop interfaces, and exported type aliases.

Run \`pnpm docs:reference:sync\` after changing public React component props.

${sections.join("\n\n")}
`;

if (shouldWrite) {
  await mkdir(path.dirname(outputPath), { recursive: true });
  await writeFile(outputPath, generated);
} else {
  const current = await readFile(outputPath, "utf8");

  if (current !== generated) {
    console.error(
      "docs/reference/react-components.md is out of sync. Run pnpm docs:reference:sync."
    );
    process.exit(1);
  }
}

async function generateComponentSection(fileName) {
  const filePath = path.join(componentsDir, fileName);
  const source = await readFile(filePath, "utf8");
  const sourceFile = ts.createSourceFile(
    filePath,
    source,
    ts.ScriptTarget.Latest,
    true,
    ts.ScriptKind.TSX
  );
  const componentId = fileName.replace(/\.tsx$/, "");
  const exports = [];
  const interfaces = [];
  const aliases = [];

  for (const statement of sourceFile.statements) {
    if (ts.isVariableStatement(statement) && hasExportModifier(statement)) {
      for (const declaration of statement.declarationList.declarations) {
        if (ts.isIdentifier(declaration.name)) {
          exports.push(declaration.name.text);
        }
      }
    }

    if (ts.isFunctionDeclaration(statement) && hasExportModifier(statement)) {
      if (statement.name) {
        exports.push(statement.name.text);
      }
    }

    if (ts.isInterfaceDeclaration(statement) && hasExportModifier(statement)) {
      interfaces.push({
        extendsText: heritageText(statement, sourceFile),
        name: statement.name.text,
        props: propertyRows(statement.members, sourceFile)
      });
    }

    if (ts.isTypeAliasDeclaration(statement) && hasExportModifier(statement)) {
      aliases.push({
        name: statement.name.text,
        type: nodeText(statement.type, sourceFile)
      });
    }
  }

  const lines = [
    `## ${titleFor(componentId)}`,
    "",
    `Source: [packages/react/src/components/${fileName}](../../packages/react/src/components/${fileName})`,
    "",
    "Component contract: " +
      `[docs/agent/components/${componentId}.md](../agent/components/${componentId}.md)`,
    "",
    "### Exports",
    "",
    exports.length > 0
      ? exports.map((exportName) => `- \`${exportName}\``).join("\n")
      : "- None"
  ];

  if (interfaces.length > 0) {
    lines.push("", "### Prop Interfaces");

    for (const details of interfaces) {
      lines.push("", `#### ${details.name}`);

      if (details.extendsText.length > 0) {
        lines.push("", `Extends: \`${escapeInlineCode(details.extendsText)}\``);
      }

      if (details.props.length === 0) {
        lines.push("", "No own props; inherits from the extended type.");
      } else {
        lines.push("", "| Prop | Required | Type | Notes |");
        lines.push("| --- | --- | --- | --- |");

        for (const prop of details.props) {
          lines.push(
            `| \`${prop.name}\` | ${prop.required ? "yes" : "no"} | \`${escapeTableCode(
              prop.type
            )}\` | ${escapeTableText(prop.notes)} |`
          );
        }
      }
    }
  }

  if (aliases.length > 0) {
    lines.push("", "### Exported Types", "", "| Type | Definition |");
    lines.push("| --- | --- |");

    for (const alias of aliases) {
      lines.push(
        `| \`${alias.name}\` | \`${escapeTableCode(alias.type)}\` |`
      );
    }
  }

  return lines.join("\n");
}

function hasExportModifier(node) {
  return Boolean(
    node.modifiers?.some((modifier) => modifier.kind === ts.SyntaxKind.ExportKeyword)
  );
}

function propertyRows(members, sourceFile) {
  return members
    .filter((member) => ts.isPropertySignature(member))
    .map((member) => ({
      name: propertyName(member.name),
      notes: jsDocText(member),
      required: !member.questionToken,
      type: member.type ? nodeText(member.type, sourceFile) : "unknown"
    }));
}

function propertyName(name) {
  if (ts.isIdentifier(name) || ts.isStringLiteral(name) || ts.isNumericLiteral(name)) {
    return name.text;
  }

  return name.getText();
}

function heritageText(node, sourceFile) {
  return (
    node.heritageClauses
      ?.flatMap((clause) => clause.types.map((type) => nodeText(type, sourceFile)))
      .join(", ") ?? ""
  );
}

function nodeText(node, sourceFile) {
  return node
    .getText(sourceFile)
    .replace(/\s+/g, " ")
    .replace(/ ,/g, ",")
    .trim();
}

function jsDocText(node) {
  const docs = ts.getJSDocCommentsAndTags(node)
    .map((doc) => doc.getText())
    .join(" ")
    .replace(/^\/\*\*|\*\/$/g, "")
    .replace(/\s*\*\s?/g, " ")
    .trim();

  return docs || "-";
}

function titleFor(componentId) {
  return componentId
    .split("-")
    .map((part) => `${part.charAt(0).toUpperCase()}${part.slice(1)}`)
    .join("");
}

function escapeInlineCode(value) {
  return value.replace(/`/g, "\\`");
}

function escapeTableCode(value) {
  return escapeInlineCode(value).replace(/\|/g, "\\|");
}

function escapeTableText(value) {
  return value.replace(/\|/g, "\\|").replace(/\n/g, " ");
}
