import { access, mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const root = process.cwd();
const componentsDir = path.join(root, "packages/react/src/components");
const docsDir = path.join(root, "docs/agent/components");
const previewsDir = path.join(root, "examples/react/src/component-previews");
const testPath = path.join(root, "packages/react/src/components/components.test.tsx");
const outputPath = path.join(root, "docs/reference/supported-surface-matrix.md");
const shouldWrite = process.argv.includes("--write");
const shouldCheck = process.argv.includes("--check");

if (!shouldWrite && !shouldCheck) {
  console.error(
    "Usage: node scripts/generate-supported-surface-matrix.mjs --write|--check"
  );
  process.exit(1);
}

const componentFiles = (await readdir(componentsDir))
  .filter((fileName) => fileName.endsWith(".tsx"))
  .filter((fileName) => !fileName.endsWith(".test.tsx"))
  .sort();
const testSource = await readFile(testPath, "utf8");

const coverageNotes = {
  "bottom-sheet":
    "Modal role, naming, description, close path, Escape dismissal, return focus, portal composition.",
  dialog:
    "Modal role, naming, description, close path, Escape dismissal, return focus, portal composition.",
  menu:
    "Menu roles, trigger opening, disabled item state, Escape dismissal, checked/radio item state.",
  popover:
    "Trigger opening, contextual content, close control, Escape dismissal, optional arrow.",
  select:
    "Combobox/listbox roles, invalid ARIA mapping, keyboard opening, option state, disabled item state.",
  tabs:
    "Tablist/tab/tabpanel roles, active state, click interaction, variant styling.",
  toast:
    "Viewport, tone, action and close controls, default accessible dismiss label, keyboard focus."
};

const maturityOverrides = {
  "bottom-sheet": "Hardened starter",
  dialog: "Hardened starter",
  menu: "Hardened starter",
  popover: "Hardened starter",
  select: "Hardened starter",
  tabs: "Hardened starter",
  toast: "Hardened starter"
};

const rows = [];

for (const fileName of componentFiles) {
  const id = fileName.replace(/\.tsx$/, "");
  const docPath = path.join(docsDir, `${id}.md`);
  const previewPath = path.join(previewsDir, `${id}.preview.tsx`);
  const sourcePath = `packages/react/src/components/${fileName}`;
  const title = titleFor(id);
  const docExists = await exists(docPath);
  const previewExists = await exists(previewPath);
  const testHit = testSource.includes(title) || testSource.includes(toExportName(id));
  const limitations = docExists
    ? knownLimitations(await readFile(docPath, "utf8"))
    : "No component contract found.";

  rows.push({
    a11y: coverageNotes[id] ?? inferredCoverage(id, testHit),
    docs: docExists ? `[contract](../agent/components/${id}.md)` : "missing",
    gaps: limitations,
    maturity: maturityOverrides[id] ?? (testHit ? "Starter" : "Uncovered"),
    preview: previewExists
      ? `[preview](../../examples/react/src/component-previews/${id}.preview.tsx)`
      : "missing",
    source: `[source](../../${sourcePath})`,
    tests: testHit
      ? "[unit](../../packages/react/src/components/components.test.tsx), [browser smoke](../../scripts/check-react-preview-browser.mjs)"
      : "missing unit reference",
    title
  });
}

const generated = `# Supported Surface Matrix

<!-- Generated by scripts/generate-supported-surface-matrix.mjs. Do not edit by hand. -->

This matrix records the currently supported React component surface. It is a
readiness view, not a promise that every component is production complete.

Maturity labels:

- **Hardened starter**: source, contract, preview, unit tests, browser smoke
  coverage, and targeted a11y/focus/interaction assertions exist for the risky
  primitive behavior.
- **Starter**: source, contract, preview, unit tests, and browser smoke coverage
  exist, with a11y behavior documented or covered at the contract/state level.
- **Uncovered**: a required source, contract, preview, or unit reference is
  missing and the component should not be considered supported.

| Component | Maturity | Source | Docs | Preview | Tests | A11y / focus coverage | Known gaps |
| --- | --- | --- | --- | --- | --- | --- | --- |
${rows
  .map(
    (row) =>
      `| ${row.title} | ${row.maturity} | ${row.source} | ${row.docs} | ${row.preview} | ${row.tests} | ${escapeTableText(
        row.a11y
      )} | ${escapeTableText(row.gaps)} |`
  )
  .join("\n")}

## Maintenance

- Run \`pnpm docs:reference:sync\` after adding or changing component source,
  previews, or known limitations.
- Keep deeper interaction coverage on the hardened starter path for Dialog,
  BottomSheet, Menu, Select, Popover, Toast, and Tabs.
- Add true browser-level focus-trap assertions only when the preview harness can
  drive modal focus without making every story stateful.
`;

if (shouldWrite) {
  await mkdir(path.dirname(outputPath), { recursive: true });
  await writeFile(outputPath, generated);
} else {
  const current = await readFile(outputPath, "utf8");

  if (current !== generated) {
    console.error(
      "docs/reference/supported-surface-matrix.md is out of sync. Run pnpm docs:reference:sync."
    );
    process.exit(1);
  }
}

async function exists(filePath) {
  try {
    await access(filePath);
    return true;
  } catch {
    return false;
  }
}

function knownLimitations(source) {
  const match = /## Known Limitations\s+([\s\S]*?)(?:\n## |\n# |$)/.exec(source);

  if (!match) {
    return "Known limitations section missing.";
  }

  const bullets = [];
  let current = null;

  for (const rawLine of match[1].split(/\r?\n/)) {
    const line = rawLine.trim();

    if (!line) {
      continue;
    }

    if (line.startsWith("- ")) {
      if (current) {
        bullets.push(current);
      }

      current = line.replace(/^- /, "");
      continue;
    }

    if (current) {
      current = `${current} ${line}`;
    }
  }

  if (current) {
    bullets.push(current);
  }

  const selectedBullets = bullets.slice(0, 3);

  return selectedBullets.length > 0
    ? selectedBullets.join("; ")
    : "No known limitations listed.";
}

function inferredCoverage(id, testHit) {
  if (!testHit) {
    return "No unit assertion found in the shared component test file.";
  }

  if (
    [
      "button",
      "checkbox",
      "radio-group",
      "switch",
      "tooltip",
      "action-menu"
    ].includes(id)
  ) {
    return "Interactive roles, labels, state attributes, and keyboard/focus behavior are covered by unit tests or Radix-backed contracts.";
  }

  return "Slots, roles or ARIA state where applicable, content resilience, and browser preview layout are covered.";
}

function titleFor(componentId) {
  return componentId
    .split("-")
    .map((part) => `${part.charAt(0).toUpperCase()}${part.slice(1)}`)
    .join("");
}

function toExportName(componentId) {
  return titleFor(componentId);
}

function escapeTableText(value) {
  return value.replace(/\|/g, "\\|").replace(/\n/g, " ");
}
