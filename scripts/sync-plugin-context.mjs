import {
  existsSync,
  mkdirSync,
  readdirSync,
  readFileSync,
  rmSync,
  statSync,
  writeFileSync
} from "node:fs";
import { Buffer } from "node:buffer";
import path from "node:path";

const root = process.cwd();
const contextRoot = path.join(root, "plugins/pds/context");
const check = process.argv.includes("--check");
const allowedExtensions = new Set([".css", ".md", ".png", ".ts"]);
const expected = new Map();
const skippedContextPaths = new Set([
  "docs/agent/evaluation-scenarios.md",
  "docs/agent/readiness-audit.md"
]);

const contextReadme = `# PDS Plugin Context

This directory is generated by \`scripts/sync-plugin-context.mjs\`.
Canonical source files live in the repository root, \`docs\`, and \`packages\`.
Run \`pnpm plugin:context\` after changing agent-facing PDS guidance.
When working inside the source repository, read the canonical files instead of
this generated copy.
`;

addGeneratedFile("README.md", contextReadme);
addFile("AGENTS.md");
addFile("CLAUDE.md");
addFile("DESIGN.md");
addFile("README.md");
addDirectory("docs/agent");
addDirectory("docs/foundations");
addDirectory("docs/reference");
addDirectory("docs/recipes");
addFile("docs/release-policy.md");
addFile("packages/README.md");
addFile("packages/cli/README.md");
addFile("packages/react/README.md");
addFile("packages/tokens/README.md");
addDirectory("packages/tokens/src");

if (check) {
  checkContext();
} else {
  writeContext();
}

function addGeneratedFile(relativePath, content) {
  expected.set(toPosix(relativePath), Buffer.from(content, "utf8"));
}

function addFile(relativePath) {
  expected.set(toPosix(relativePath), readFileSync(path.join(root, relativePath)));
}

function addDirectory(relativePath) {
  const absolutePath = path.join(root, relativePath);

  for (const entry of readdirSync(absolutePath, { withFileTypes: true })) {
    const entryRelativePath = path.join(relativePath, entry.name);
    const posixEntryRelativePath = toPosix(entryRelativePath);

    if (skippedContextPaths.has(posixEntryRelativePath)) {
      continue;
    }

    if (entry.isDirectory()) {
      addDirectory(entryRelativePath);
      continue;
    }

    if (entry.isFile() && allowedExtensions.has(path.extname(entry.name))) {
      addFile(entryRelativePath);
    }
  }
}

function writeContext() {
  rmSync(contextRoot, { force: true, recursive: true });

  for (const [relativePath, content] of expected) {
    const targetPath = path.join(contextRoot, relativePath);
    mkdirSync(path.dirname(targetPath), { recursive: true });
    writeFileSync(targetPath, content);
  }

  console.log(`Synced ${expected.size} plugin context files.`);
}

function checkContext() {
  const failures = [];
  const actualFiles = existsSync(contextRoot) ? collectFiles(contextRoot) : [];
  const actualRelativePaths = new Set(actualFiles.map((file) => toPosix(path.relative(contextRoot, file))));

  for (const [relativePath, content] of expected) {
    const targetPath = path.join(contextRoot, relativePath);

    if (!existsSync(targetPath)) {
      failures.push(`${relativePath}: missing from plugin context`);
      continue;
    }

    const actual = readFileSync(targetPath);

    if (!actual.equals(content)) {
      failures.push(`${relativePath}: out of sync`);
    }
  }

  for (const relativePath of actualRelativePaths) {
    if (!expected.has(relativePath)) {
      failures.push(`${relativePath}: stale generated file`);
    }
  }

  if (failures.length > 0) {
    console.error("PDS plugin context is out of sync:");
    for (const failure of failures) {
      console.error(`- ${failure}`);
    }
    console.error("Run pnpm plugin:context to regenerate it.");
    process.exitCode = 1;
    return;
  }

  console.log(`PDS plugin context is in sync (${expected.size} files).`);
}

function collectFiles(directory) {
  const files = [];

  for (const entry of readdirSync(directory, { withFileTypes: true })) {
    const absolutePath = path.join(directory, entry.name);

    if (entry.isDirectory()) {
      files.push(...collectFiles(absolutePath));
      continue;
    }

    if (entry.isFile() && statSync(absolutePath).isFile()) {
      files.push(absolutePath);
    }
  }

  return files.sort();
}

function toPosix(value) {
  return value.split(path.sep).join("/");
}
